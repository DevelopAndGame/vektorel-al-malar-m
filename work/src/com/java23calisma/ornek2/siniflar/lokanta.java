	String telNumarasi;
	
	public void setAdi(String adi) {
		this.adi = adi;
	}
	
	public void setAdresi(String adresi) {
		this.adresi = adresi;
	}
	
	public void setMarkasi(String markasi) {
		this.markasi = markasi;
	}
	
	public void setTelNumarasi(String telNumarasi) {
		this.telNumarasi = telNumarasi;
	}
	
	public String getAdi() {
		return adi;
	}
	
	public String getAdresi() {
		return adresi;
	}
	
	public String getMarkasi() {
		return markasi;
	}
	
	public String getTelNumarasi() {
		return telNumarasi;
	}
	
